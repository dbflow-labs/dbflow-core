declare(strict_types=1);

namespace DbflowLabs\Core\Jobs;

use DbflowLabs\Core\Services\Sla\ProcessSlaEvent;
use Illuminate\Bus\Queueable;
use Illuminate\Contracts\Queue\ShouldQueue;
use Illuminate\Foundation\Bus\Dispatchable;
use Illuminate\Queue\InteractsWithQueue;
use Illuminate\Queue\SerializesModels;

final class ProcessSlaEventJob implements ShouldQueue
{
    use Dispatchable;
    use InteractsWithQueue;
    use Queueable;
    use SerializesModels;

    public int $tries = 1;

    public function __construct(
        public readonly int $slaEventId,
        public readonly int $expectedAttempt,
    ) {}

    public function handle(ProcessSlaEvent $processor): void
    {
        $processor->handle($this->slaEventId, $this->expectedAttempt);
    }
}

